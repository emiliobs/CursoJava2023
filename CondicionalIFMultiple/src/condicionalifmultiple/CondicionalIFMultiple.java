
package condicionalifmultiple;


public class CondicionalIFMultiple 
{

    
    public static void main(String[] args) 
    {
       int num1 = 5;
       int num2 = 55;
       
        if (num1 > num2)
        {
            System.out.println("El num1 es mayor que num2");
        }
        else if (num1 == num2)
        {
            System.out.println("El num1 es igual a num2");
        }
        else
        {
            System.out.println("El num2 es mayor que num1");
        }
    }
    
}
