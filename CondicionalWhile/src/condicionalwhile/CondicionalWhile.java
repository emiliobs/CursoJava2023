
package condicionalwhile;


public class CondicionalWhile 
{
  
    public static void main(String[] args)
    {
        int contador = 0;
                
        System.out.println("Star Bucle");
        while (contador <= 10) 
        {            
            
            System.out.println("Estoy en la vuelta " + contador);
            contador += 1;
        }
        
        System.out.println("Fin del Bucle");
    }
    
}
